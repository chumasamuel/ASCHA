    <p>A Associação Sócio Cultural Horizonte Azul (ASCHA) é, uma pessoa coletiva, jurídica e juvenil que congrega maioritariamente mulheres jovens e raparigas que militam na defesa dos direitos humanos das mulheres jovens e raparigas, na promoção da igualdade de género, do empoderamento político, cultural, social e económico, da participação política e do engajamento cívico, em particular das que estão em situação de risco e vulnerabilidade nas comunidades per urbanas e rurais, no sentido de reduzir as barreiras que estas enfrentam no controlo das 3 dimensões do corpo (força de trabalho, sexualidade e reprodução). Defende uma identidade feminista de igualdade onde raparigas e rapazes, mulheres e homens têm as mesmas oportunidades de aceder recursos afetivos, materiais e financeiros que garantem a sua autonomia e melhorem as condições de sujeitas e sujeitos de direitos nas Comunidades. </p>
    <p>Missão:<br>
Promover o desenvolvimento local através da educação, formação, comunicação, arte, cultura e advocacia para eliminação de todas as práticas sociais, culturais, religiosas e tradicionais nocivas a saúde das raparigas e mulheres jovens nas comunidades de Moçambique.</p>
<p>Visão:<br>
Ser uma organização habilitada na abordagem de género e direitos humanos das raparigas e mulheres jovens para que exerçam os seus direitos livres de qualquer tipo de violência, opressão e discriminação.</p>
<p>Valores:<br>
Amor, Empatia, honestidade, igualdade, justiça social, perseverança, sonhos, respeito pela pessoa humana, trabalho em equipa, sororidade, solidariedade e trabalho.</p>

